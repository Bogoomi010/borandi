/** key로 단일 에셋 조회 */
export function getUiAsset(key: string): UiAsset | undefined {
  return UI_ASSETS.find((a) => a.key === key);
}

/** 카테고리별 그룹 조회 */
export function uiAssetsByCategory(category: AssetCategory): UiAsset[] {
  return UI_ASSETS.filter((a) => a.category === category);
}

/** 원본 시트별 그룹 조회 */
export function uiAssetsBySheet(sheet: SheetId): UiAsset[] {
  return UI_ASSETS.filter((a) => a.sheet === sheet);
}
