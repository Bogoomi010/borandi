/** 슬라이스 후 저장될 카테고리 폴더 (src/assets/ui/<category>/) */
export type AssetCategory =
  | "frames" //       패널/나인슬라이스/코너/디바이더/배지 베이스
  | "buttons" //      액션·탭·범용 버튼, 속도 버튼
  | "topbar" //       상단 상태바, 정보 배지, 헤더 아이콘
  | "units" //        유닛 카드, 필터, 속성/등급 아이콘, 용량 바
  | "unit-detail" //  선택 유닛 상세 패널 부품
  | "mission" //      미션 카드, 배너, 플레이트
  | "boss" //         보스 정보/카드/보상/경고
  | "battlefield" //  전장 프레임, 길, 마커, 웨이브 노드
  | "meters" //       데미지미터, HP/MP/보스 바, 진행 바
  | "slots" //        스킬/아이템/장비/유물/효과/빈/잠금 슬롯
  | "popups" //       확인/보상/설정/툴팁/보스 경고 팝업
  | "controls" //     체크박스, 토글, 커서
  | "fx"; //          글로우 등 이펙트

export interface UiAsset {
  /** 코드에서 쓰는 고유 키 (점 네임스페이스) */
  key: string;
  /** 사람이 읽는 이름 */
  label: string;
  sheet: SheetId;
  category: AssetCategory;
  /** 슬라이스 결과물이 놓일 경로 (src/assets/ui 기준 상대) */
  file: string;
  /** 적용 위치: src/ui 모듈과 렌더 함수(COMPONENT) */
  usage: {
    module: string;
    component: string;
    /** 어떤 상태/용도로 쓰이는지 */
    note: string;
  }[];
  /** 9-슬라이스로 늘려 쓰는 프레임/바 계열인가 */
  nineSlice?: boolean;
  /**
   * 9-슬라이스 모서리 inset(px). 늘어나지 않는 테두리 두께.
   * 단일 값이면 4변 동일, [상,우,하,좌]면 변별 지정. 미지정 시 헬퍼 기본값 사용.
   * 슬라이스 후 실제 에셋에 맞게 채운다.
   */
  border?: number | [number, number, number, number];
  /** 동일 비주얼의 상태 변형(normal/hover/selected 등) 묶음 키 */
  variantGroup?: string;
  /** 실제 PNG 슬라이스 완료 여부 */
  sliced: boolean;
}

